package exercicio_03_jogo_de_adivinhacao;

public class Player {
	private String nome;
	private int palpite;
	
	public Player() {
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public int getPalpite() {
		return palpite;
	}

	public void setPalpite(int palpite) {
		this.palpite = palpite;
	}
	
	
	
}
